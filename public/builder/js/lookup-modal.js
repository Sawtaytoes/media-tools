// ─── Render helpers ───────────────────────────────────────────────────────────

function renderLookupModal() {
  const titleEl = document.getElementById('lookup-title')
  const backBtn = document.getElementById('lookup-back-btn')
  const body = document.getElementById('lookup-body')
  if (!lookupState) return

  titleEl.textContent = LOOKUP_TITLES[lookupState.lookupType] ?? 'Lookup'

  if (lookupState.stage === 'search') {
    backBtn.classList.add('hidden')
    body.innerHTML = renderLookupSearchView()
  } else if (lookupState.stage === 'variant') {
    backBtn.classList.remove('hidden')
    body.innerHTML = renderLookupVariantView()
  } else if (lookupState.stage === 'release') {
    backBtn.classList.remove('hidden')
    body.innerHTML = renderLookupReleaseView()
  }
}

function renderLookupSearchView() {
  const isDvdCompare = lookupState.lookupType === 'dvdcompare'
  const filterOptions = [
    { value: 'all', label: 'All' },
    { value: 'DVD', label: 'DVD' },
    { value: 'Blu-ray', label: 'Blu-ray' },
    { value: 'Blu-ray 4K', label: 'UHD Blu-ray' },
  ]
  const filters = isDvdCompare
    ? `<div class="flex items-center gap-3 text-xs text-slate-400 flex-wrap">
        <span class="font-medium">Format:</span>
        ${filterOptions.map(opt => `
          <label class="flex items-center gap-1 cursor-pointer">
            <input type="radio" name="lookup-format-filter" value="${opt.value}"
              ${lookupState.formatFilter === opt.value ? 'checked' : ''}
              onchange="setLookupFormatFilter(this.value)"
              class="w-3.5 h-3.5 bg-slate-700 border-slate-500 accent-blue-500" />
            <span>${esc(opt.label)}</span>
          </label>
        `).join('')}
      </div>`
    : ''

  // Reset _activeOptions; result renderers populate them.
  lookupState._activeOptions = []

  let resultsHtml = ''
  if (lookupState.loading) {
    resultsHtml = '<p class="text-xs text-slate-500 text-center py-4">Searching…</p>'
  } else if (lookupState.searchError) {
    resultsHtml = `<div class="text-xs text-red-300 bg-red-950/40 border border-red-900/60 rounded px-3 py-2 font-mono break-words">
      <p class="text-red-400 font-medium mb-1">Search failed</p>
      <p>${esc(lookupState.searchError)}</p>
    </div>`
  } else if (lookupState.results === null) {
    resultsHtml = '<p class="text-xs text-slate-500 text-center py-4">Enter a search term and press Enter.</p>'
  } else if (lookupState.results.length === 0) {
    resultsHtml = '<p class="text-xs text-slate-500 text-center py-4">No results found.</p>'
  } else if (isDvdCompare) {
    resultsHtml = renderDvdCompareSearchResults()
  } else {
    resultsHtml = renderSimpleSearchResults()
  }

  return `
    <div class="flex items-center gap-2">
      <input id="lookup-search-input" type="text" value="${esc(lookupState.searchTerm)}" placeholder="Title to search…"
        oninput="setLookupSearchTerm(this.value)"
        onkeydown="if(event.key==='Enter'){event.preventDefault();runLookupSearch(this.value)}"
        class="flex-1 bg-slate-700 text-slate-200 text-xs rounded px-2 py-1.5 border border-slate-600 focus:outline-none focus:border-blue-500" />
      <button onclick="runLookupSearch(document.getElementById('lookup-search-input').value)"
        class="text-xs bg-blue-600 hover:bg-blue-500 text-white px-3 py-1.5 rounded font-medium shrink-0">Search</button>
    </div>
    ${filters}
    <div class="flex flex-col gap-1.5 overflow-y-auto min-h-0">${resultsHtml}</div>
  `
}

function kbdBadge(index) {
  return index < 9
    ? `<kbd class="text-xs font-mono bg-slate-700 text-slate-300 px-1.5 py-0.5 rounded border border-slate-600 shrink-0">${index + 1}</kbd>`
    : ''
}

// User-facing label for DVDCompare format variants.
function displayDvdCompareVariant(variant) {
  return variant === 'Blu-ray 4K' ? 'UHD Blu-ray' : variant
}

// Pulls baseTitle + year out of a dvdCompareName like "Soldier (UHD Blu-ray) (1998)".
export function parseDvdCompareDisplayName(displayName) {
  if (!displayName) return null
  const yearMatch = displayName.match(/\s*\((\d{4})\)\s*$/)
  const withoutYear = yearMatch
    ? displayName.slice(0, yearMatch.index).trim()
    : displayName.trim()
  const variantMatch = withoutYear.match(/\s*\((?:UHD Blu-ray|Blu-ray 4K|Blu-ray|DVD)\)\s*$/i)
  const baseTitle = variantMatch
    ? withoutYear.slice(0, variantMatch.index).trim()
    : withoutYear
  return { baseTitle, year: yearMatch?.[1] || '' }
}

function renderSimpleSearchResults() {
  lookupState._activeResults = lookupState.results
  lookupState._activeOptions = lookupState.results.map((_, index) => () => lookupSelectSimpleByIndex(index))
  return lookupState.results.map((r, index) => {
    let label, id
    if (lookupState.lookupType === 'mal') {
      id = r.malId
      label = `${esc(r.name)}${r.airDate ? ` <span class="text-slate-500">(${esc(r.airDate)})</span>` : ''}${r.mediaType ? ` <span class="text-slate-500">[${esc(r.mediaType)}]</span>` : ''}`
    } else if (lookupState.lookupType === 'anidb') {
      id = r.aid
      label = `${esc(r.name)}${r.type ? ` <span class="text-slate-500">[${esc(r.type)}]</span>` : ''}${r.episodes ? ` <span class="text-slate-500">(${r.episodes} ep)</span>` : ''}`
    } else if (lookupState.lookupType === 'tvdb') {
      id = r.tvdbId
      label = `${esc(r.name)}${r.year ? ` <span class="text-slate-500">(${esc(r.year)})</span>` : ''}${r.status ? ` <span class="text-slate-500">[${esc(r.status)}]</span>` : ''}`
    } else if (lookupState.lookupType === 'tmdb') {
      id = r.movieDbId
      label = `${esc(r.title)}${r.year ? ` <span class="text-slate-500">(${esc(r.year)})</span>` : ''}`
    }
    return `<button onclick="lookupSelectSimpleByIndex(${index})"
      class="text-left text-sm px-3 py-2 rounded-lg border border-slate-600 text-slate-200 hover:bg-blue-700 hover:border-blue-500 transition-colors flex items-center gap-2">
      ${kbdBadge(index)}
      <span class="flex-1 min-w-0 truncate">${label}</span>
      <span class="text-xs font-mono text-slate-500 shrink-0">#${id}</span>
    </button>`
  }).join('')
}

function renderDvdCompareSearchResults() {
  const groups = new Map()
  for (const r of lookupState.results) {
    if (lookupState.formatFilter !== 'all' && lookupState.formatFilter !== r.variant) continue
    const key = `${r.baseTitle}|${r.year}`
    if (!groups.has(key)) groups.set(key, { baseTitle: r.baseTitle, year: r.year, variants: [] })
    groups.get(key).variants.push({ id: r.id, variant: r.variant })
  }

  if (groups.size === 0) {
    return '<p class="text-xs text-slate-500 text-center py-4">No results match the selected format.</p>'
  }

  const groupArray = Array.from(groups.values())
  lookupState._renderedGroups = groupArray
  lookupState._activeOptions = groupArray.map((_, index) => () => lookupSelectGroup(index))

  return groupArray.map((group, index) => {
    const variantBadges = group.variants.map(v => `<span class="text-xs font-mono px-1.5 py-0.5 rounded bg-slate-700 text-slate-300">${esc(displayDvdCompareVariant(v.variant))}</span>`).join(' ')
    return `<button onclick="lookupSelectGroup(${index})"
      class="text-left text-sm px-3 py-2 rounded-lg border border-slate-600 text-slate-200 hover:bg-blue-700 hover:border-blue-500 transition-colors flex items-center gap-2 flex-wrap">
      ${kbdBadge(index)}
      <span class="font-medium">${esc(group.baseTitle)}</span>
      ${group.year ? `<span class="text-slate-500">(${esc(group.year)})</span>` : ''}
      <span class="ml-auto flex gap-1">${variantBadges}</span>
    </button>`
  }).join('')
}

