// ─── Search action ────────────────────────────────────────────────────────────

export async function runLookupSearch(searchTerm) {
  if (!searchTerm || !lookupState) return
  lookupState.searchTerm = searchTerm
  lookupState.searchError = null
  document.getElementById('lookup-search-input')?.blur()
  lookupState.loading = true
  renderLookupModal()
  try {
    const endpoint = lookupState.lookupType === 'mal' ? '/queries/searchMal'
                   : lookupState.lookupType === 'anidb' ? '/queries/searchAnidb'
                   : lookupState.lookupType === 'tvdb' ? '/queries/searchTvdb'
                   : lookupState.lookupType === 'tmdb' ? '/queries/searchMovieDb'
                   : '/queries/searchDvdCompare'
    const resp = await fetch(endpoint, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ searchTerm }),
    })
    const data = await resp.json()
    lookupState.results = data.results ?? []
    lookupState.searchError = data.error ?? null
    if (data.error) console.error('[lookup search]', data.error)
  } catch (err) {
    lookupState.results = []
    lookupState.searchError = err?.message ?? String(err)
    console.error('Lookup search failed', err)
  }
  lookupState.loading = false
  renderLookupModal()
}

// ─── Selection handlers ───────────────────────────────────────────────────────

export function lookupSelectSimpleByIndex(index) {
  if (!lookupState) return
  const r = lookupState._activeResults?.[index]
  if (!r) return
  let id, displayName
  if (lookupState.lookupType === 'mal') {
    id = r.malId
    displayName = r.name
  } else if (lookupState.lookupType === 'anidb') {
    id = r.aid
    displayName = r.name
  } else if (lookupState.lookupType === 'tvdb') {
    id = r.tvdbId
    displayName = r.name
  } else if (lookupState.lookupType === 'tmdb') {
    id = r.movieDbId
    displayName = r.year ? `${r.title} (${r.year})` : r.title
  } else {
    id = r.tvdbId
    displayName = r.name
  }
  const step = findStepById(lookupState.stepId)
  if (step) {
    const cmd = COMMANDS[step.command]
    const field = cmd?.fields.find(f => f.name === lookupState.fieldName)
    step.params[lookupState.fieldName] = id
    if (field?.companionNameField && displayName) {
      step.params[field.companionNameField] = displayName
    }
    refreshLinkedInputs()
    updateYaml()
    renderAll()
  }
  closeLookupModal()
}

export function lookupSelectGroup(index) {
  if (!lookupState) return
  const group = lookupState._renderedGroups?.[index]
  if (!group) return
  lookupState.selectedGroup = group
  // If only one variant, skip straight to release stage
  if (group.variants.length === 1) {
    lookupSelectVariant(group.variants[0].id, group.variants[0].variant)
    return
  }
  lookupState.stage = 'variant'
  renderLookupModal()
}

export async function lookupSelectVariant(fid, variant) {
  if (!lookupState) return
  lookupState.selectedFid = fid
  lookupState.selectedVariant = variant ?? null
  lookupState.stage = 'release'
  lookupState.releases = null
  lookupState.releasesDebug = null
  lookupState.releasesError = null
  lookupState.loading = true
  renderLookupModal()
  try {
    const resp = await fetch('/queries/listDvdCompareReleases', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ dvdCompareId: fid }),
    })
    const data = await resp.json()
    lookupState.releases = data.releases ?? []
    lookupState.releasesDebug = data.debug ?? null
    lookupState.releasesError = data.error ?? null
    if (data.error) {
      console.error('[listDvdCompareReleases] error', data.error)
    } else if (lookupState.releases.length === 0) {
      console.warn('[listDvdCompareReleases] no releases', data.debug)
    }
  } catch (err) {
    lookupState.releases = []
    lookupState.releasesError = err?.message ?? String(err)
    console.error('listDvdCompareReleases failed', err)
  }
  lookupState.loading = false

  // Single release — auto-select it.
  if (lookupState.releases && lookupState.releases.length === 1) {
    const only = lookupState.releases[0]
    lookupSelectRelease(only.hash, only.label)
    return
  }

  renderLookupModal()
}

export function lookupSelectReleaseByIndex(index) {
  if (!lookupState) return
  const release = lookupState.releases?.[index]
  if (!release) return
  lookupSelectRelease(release.hash, release.label)
}

export function lookupSelectRelease(hash, label) {
  if (!lookupState) return
  const step = findStepById(lookupState.stepId)
  if (step) {
    step.params.dvdCompareId = lookupState.selectedFid
    step.params.dvdCompareReleaseHash = Number(hash) || hash
    const group = lookupState.selectedGroup
    if (group) {
      const variantSuffix = lookupState.selectedVariant && lookupState.selectedVariant !== 'DVD'
        ? ` (${displayDvdCompareVariant(lookupState.selectedVariant)})` : ''
      const yearSuffix = group.year ? ` (${group.year})` : ''
      step.params.dvdCompareName = `${group.baseTitle}${variantSuffix}${yearSuffix}`
    }
    if (label) step.params.dvdCompareReleaseLabel = label
    clearTmdbResolution(step)
    if (group?.baseTitle && step.command === 'nameSpecialFeatures') {
      step.params.tmdbResolutionPending = true
    }
    refreshLinkedInputs()
    updateYaml()
    renderAll()
    if (group?.baseTitle && step.command === 'nameSpecialFeatures') {
      resolveTmdbForStep(step.id, group.baseTitle, group.year || '')
    }
  }
  closeLookupModal()
}

// ─── Keyboard handler ─────────────────────────────────────────────────────────

let lookupKeydownHandler = null

function installLookupKeyboardHandler() {
  uninstallLookupKeyboardHandler()
  lookupKeydownHandler = (event) => {
    if (!lookupState) return
    if (event.key === 'Escape') {
      closeLookupModal()
      return
    }
    if (event.target?.tagName === 'INPUT' || event.target?.tagName === 'TEXTAREA') return
    if (event.key >= '1' && event.key <= '9') {
      const index = Number(event.key) - 1
      const handler = lookupState._activeOptions?.[index]
      if (handler) {
        event.preventDefault()
        handler()
      }
    }
  }
  document.addEventListener('keydown', lookupKeydownHandler)
}

function uninstallLookupKeyboardHandler() {
  if (lookupKeydownHandler) {
    document.removeEventListener('keydown', lookupKeydownHandler)
    lookupKeydownHandler = null
  }
}

// ─── TMDB resolution ──────────────────────────────────────────────────────────

export async function resolveTmdbForStep(stepId, baseTitle, year) {
  const step = steps.find((candidate) => candidate.id === stepId)
  if (!step || !baseTitle) return
  step.params.tmdbResolutionPending = true
  try {
    const response = await fetch('/queries/searchMovieDb', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ searchTerm: baseTitle, year: year || undefined }),
    })
    if (!response.ok) return
    const data = await response.json()
    const top = (data.results || [])[0]
    if (top && top.movieDbId) {
      step.params.tmdbId = top.movieDbId
      step.params.tmdbName = top.year ? `${top.title} (${top.year})` : top.title
    } else {
      delete step.params.tmdbId
      delete step.params.tmdbName
    }
  } catch (err) {
    console.error('resolveTmdbForStep failed', err)
  } finally {
    delete step.params.tmdbResolutionPending
    step.params.tmdbResolutionAttempted = true
    renderAll()
  }
}

export function clearTmdbResolution(step) {
  delete step.params.tmdbId
  delete step.params.tmdbName
  delete step.params.tmdbResolutionAttempted
}

// ─── Kick (post-load catch-ups) ───────────────────────────────────────────────

export function kickReverseLookups() {
  for (const step of steps) {
    if (!step.command) continue
    const cmd = COMMANDS[step.command]
    if (!cmd) continue
    for (const field of cmd.fields) {
      if (field.type !== 'numberWithLookup') continue
      if (!field.companionNameField) continue
      const idValue = step.params[field.name]
      const numericId = Number(idValue)
      if (!Number.isFinite(numericId) || numericId <= 0) continue
      const token = String(numericId)
      const key = `${step.id}-${field.name}`
      reverseLookupTokens.set(key, token)
      runReverseLookup(step.id, field.name, numericId, token)
    }
  }
}

export function kickTmdbResolutions() {
  for (const step of steps) {
    if (step.command !== 'nameSpecialFeatures') continue
    if (step.params.tmdbId) continue
    if (step.params.tmdbResolutionPending) continue
    if (step.params.tmdbResolutionAttempted) continue
    const parsed = parseDvdCompareDisplayName(step.params.dvdCompareName)
    if (!parsed?.baseTitle) continue
    resolveTmdbForStep(step.id, parsed.baseTitle, parsed.year)
  }
}

// ─── Reverse lookup (typed ID → fetch display name) ───────────────────────────

const reverseLookupTimers = new Map()
const reverseLookupTokens = new Map()

function setCompanionDisplay(stepId, fieldName, text) {
  const el = document.querySelector(`[data-step="${stepId}"][data-companion="${fieldName}"]`)
  if (!el) return
  el.textContent = text
  el.title = text
  if (text) el.classList.remove('hidden')
  else el.classList.add('hidden')
}

export function updateLookupLinks(stepId, fieldName, rawValue) {
  const step = steps.find((candidate) => candidate.id === stepId)
  if (!step) return
  const cmd = COMMANDS[step.command]
  const field = cmd?.fields.find((candidate) => candidate.name === fieldName)
  if (!field || field.type !== 'numberWithLookup') return
  const lookupConfig = LOOKUP_LINKS[field.lookupType]
  if (!lookupConfig) return

  const numericValue = (rawValue !== undefined && rawValue !== '') ? Number(rawValue) : null
  const hasValidId = numericValue !== null && Number.isFinite(numericValue) && numericValue > 0
  const isNameSpecialFeaturesCard = (
    step.command === 'nameSpecialFeatures'
    && field.lookupType === 'dvdcompare'
  )

  const companion = document.querySelector(
    `[data-step="${stepId}"][data-companion="${fieldName}"]`,
  )
  if (companion && companion.tagName === 'A') {
    let companionHref
    if (isNameSpecialFeaturesCard) {
      // Movie name on the left → DVDCompare release URL when an ID
      // is set; falls back to DVDCompare home otherwise. (Swapped in
      // this session — used to point at TMDB; the user wanted the
      // primary click to open DVDCompare since that's the source of
      // truth for this command.)
      companionHref = hasValidId
        ? lookupConfig.buildUrl(numericValue, { ...step.params, [fieldName]: numericValue })
        : lookupConfig.homeUrl
    } else if (hasValidId) {
      const tempParams = { ...step.params, [fieldName]: numericValue }
      companionHref = lookupConfig.buildUrl(numericValue, tempParams)
    } else {
      companionHref = lookupConfig.homeUrl
    }
    companion.setAttribute('href', companionHref)
  }

  if (field.lookupType === 'dvdcompare') {
    const rightLink = document.querySelector(
      `[data-step="${stepId}"][data-right-link="${fieldName}"]`,
    )
    if (rightLink) {
      let href
      if (isNameSpecialFeaturesCard) {
        // Right-side link is now the TMDB target. Mirrors the
        // initial-render logic in step-renderer.js.
        if (step.params.tmdbId) {
          href = `https://www.themoviedb.org/movie/${encodeURIComponent(step.params.tmdbId)}`
        } else {
          href = 'https://www.themoviedb.org/'
        }
      } else {
        href = hasValidId
          ? lookupConfig.buildUrl(numericValue, { ...step.params, [fieldName]: numericValue })
          : lookupConfig.homeUrl
      }
      rightLink.setAttribute('href', href)
    }
  }
}

export function scheduleReverseLookup(stepId, fieldName, rawValue) {
  const key = `${stepId}-${fieldName}`
  if (reverseLookupTimers.has(key)) {
    clearTimeout(reverseLookupTimers.get(key))
    reverseLookupTimers.delete(key)
  }
  reverseLookupTokens.set(key, rawValue)

  const step = findStepById(stepId)
  if (!step) return
  const cmd = COMMANDS[step.command]
  const field = cmd?.fields.find(f => f.name === fieldName)
  if (!field?.companionNameField) return
  delete step.params[field.companionNameField]
  setCompanionDisplay(stepId, fieldName, '')
  updateYaml()

  if (fieldName === 'dvdCompareId') {
    const hashField = cmd?.fields.find(f => f.name === 'dvdCompareReleaseHash')
    if (hashField?.companionNameField) {
      delete step.params[hashField.companionNameField]
      setCompanionDisplay(stepId, 'dvdCompareReleaseHash', '')
    }
    clearTmdbResolution(step)
  }

  if (!rawValue) return
  const numericValue = Number(rawValue)
  if (!Number.isFinite(numericValue) || numericValue <= 0) return

  const timeoutId = setTimeout(() => {
    reverseLookupTimers.delete(key)
    runReverseLookup(stepId, fieldName, numericValue, rawValue)
  }, 600)
  reverseLookupTimers.set(key, timeoutId)
}

async function runReverseLookup(stepId, fieldName, id, requestToken) {
  const key = `${stepId}-${fieldName}`
  const step = findStepById(stepId)
  if (!step) return
  const cmd = COMMANDS[step.command]
  const field = cmd?.fields.find(f => f.name === fieldName)
  if (!field?.companionNameField) return

  let endpoint, body
  if (field.lookupType === 'mal') {
    endpoint = '/queries/lookupMal'
    body = { malId: id }
  } else if (field.lookupType === 'anidb') {
    endpoint = '/queries/lookupAnidb'
    body = { anidbId: id }
  } else if (field.lookupType === 'tvdb') {
    endpoint = '/queries/lookupTvdb'
    body = { tvdbId: id }
  } else if (field.lookupType === 'tmdb') {
    endpoint = '/queries/lookupMovieDb'
    body = { movieDbId: id }
  } else if (field.lookupType === 'dvdcompare') {
    endpoint = '/queries/lookupDvdCompare'
    body = { dvdCompareId: id }
  } else if (fieldName === 'dvdCompareReleaseHash') {
    if (!step.params.dvdCompareId) return
    endpoint = '/queries/lookupDvdCompareRelease'
    body = { dvdCompareId: step.params.dvdCompareId, hash: String(id) }
  } else {
    return
  }

  try {
    const resp = await fetch(endpoint, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(body),
    })
    if (!resp.ok) return
    const data = await resp.json()
    const name = data.name ?? data.label
    if (!name) return

    if (reverseLookupTokens.get(key) !== requestToken) return

    step.params[field.companionNameField] = name
    if (fieldName === 'dvdCompareId' && step.command === 'nameSpecialFeatures') {
      step.params.tmdbResolutionPending = true
      setCompanionDisplay(stepId, fieldName, '')
    } else {
      setCompanionDisplay(stepId, fieldName, name)
    }
    updateYaml()
    scheduleUpdateUrl()

    if (fieldName === 'dvdCompareId') {
      const hashField = cmd?.fields.find(f => f.name === 'dvdCompareReleaseHash')
      if (hashField) {
        const hashValue = step.params.dvdCompareReleaseHash ?? hashField.default ?? 1
        const hashKey = `${stepId}-dvdCompareReleaseHash`
        const hashToken = String(hashValue)
        reverseLookupTokens.set(hashKey, hashToken)
        runReverseLookup(stepId, 'dvdCompareReleaseHash', Number(hashValue), hashToken)
      }
      if (step.command === 'nameSpecialFeatures') {
        const parsed = parseDvdCompareDisplayName(name)
        if (parsed?.baseTitle) {
          resolveTmdbForStep(stepId, parsed.baseTitle, parsed.year)
        }
      }
    }
  } catch (err) {
    console.error('Reverse lookup failed', err)
  }
}
